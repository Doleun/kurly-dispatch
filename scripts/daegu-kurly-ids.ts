export function buildNameToKurlyIdMap(): Map<string, string> {
  const map = new Map<string, string>();
  for (const line of DAEGU_KURLY_IDS_RAW.split("\n")) {
    const trimmed = line.trim();
    if (!trimmed) continue;
    const [kurlyId, displayRaw] = trimmed.split(/\t+/);
    const name = displayRaw.trim().replace(/^DW/i, "");
    if (kurlyId && name) map.set(name, kurlyId);
  }
  return map;
}
